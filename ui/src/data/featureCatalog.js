export function getFeaturesByLevel(level) {
  return FEATURE_CATALOG.filter((f) => f.level === level);
}

export function getFeaturesByDomain(domain) {
  return FEATURE_CATALOG.filter((f) => f.domain === domain);
}

export function getDefaultFeaturesForProfile(profileId) {
  const result = {};
  for (const feature of FEATURE_CATALOG) {
    if (feature.profiles.includes(profileId)) {
      result[feature.id] = true;
    }
  }
  return result;
}

export function calculateStorageRequirements(selectedFeatures) {
  let needsSrvData = false;
  let totalGb = 0;

  for (const [id, isEnabled] of Object.entries(selectedFeatures)) {
    if (isEnabled) {
      const feature = FEATURE_CATALOG.find((f) => f.id === id);
      if (feature) {
        if (feature.storage.requiresSrvData) needsSrvData = true;
        totalGb += feature.storage.estimatedDiskGb || 0;
      }
    }
  }

  return { needsSrvData, totalGb };
}
